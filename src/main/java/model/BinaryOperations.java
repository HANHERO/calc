package model;

/**
 * The enum of binary operations.
 * Binary operations are performed with two numbers.
 * @author  Pilipenko Mihail
 * @version 1.0
 */
public enum BinaryOperations {
    /**
     * Plus binary operations.
     */
    PLUS,
    /**
     * Minus binary operations.
     */
    MINUS,
    /**
     * Multiply binary operations.
     */
    MULTIPLY,
    /**
     * Divide binary operations.
     */
    DIVIDE
}
