package model;

/**
 * The enum of unary calculator operations.
 * <br/> Unary operations are performed with one numbers.
 *
 * @author Pilipenko Mihail
 * @version 1.0
 */
public enum UnaryOperations {
    /**
     * Square unary operations.
     */
    SQUARE,
    /**
     * Square root unary operations.
     */
    SQRT,
    /**
     * One divided x unary operations.
     */
    ONE_DIVIDED_X,
    /**
     * Negative unary operations.
     */
    NEGATIVE,
    /**
     * Percent unary operations.
     */
    PERCENT
}
